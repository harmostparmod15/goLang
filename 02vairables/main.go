package main

import "fmt"


func main ( )  {

	// STRING VARIABLE
var username string = "f"
fmt.Println(username)
fmt.Printf("variable is of type :  %T \n"  , username)


// BOOLEAN VARIABLE
var isLoggedIn = true;
fmt.Println(isLoggedIn)
fmt.Printf("variable is of type : %T \n " , isLoggedIn)


// INTEGER VARIABLE
var value int = 102
fmt.Println(value)
fmt.Printf("variable is of type : %T \n " , value)


// FLOAT VARIABLES
var float float32 = 23.44335
fmt.Println(float)
fmt.Printf("variable is of type : %T \n " , float)


}

